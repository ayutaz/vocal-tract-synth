  'ぱ': ['p', 'a'], 'ぴ': ['p', 'i'], 'ぷ': ['p', 'ɯ'], 'ぺ': ['p', 'e'], 'ぽ': ['p', 'o'],

  // -------------------------------------------------------------------------
  // 拗音 36 組 (2 文字、最長一致で優先)
  // -------------------------------------------------------------------------
  'きゃ': ['kj', 'a'], 'きゅ': ['kj', 'ɯ'], 'きょ': ['kj', 'o'],
  'しゃ': ['ɕ', 'a'], 'しゅ': ['ɕ', 'ɯ'], 'しょ': ['ɕ', 'o'],
