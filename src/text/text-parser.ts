// ============================================================================
// Phase 8: text-parser - ひらがな → 音素トークン変換
// ============================================================================
//
// 目的:
//   入力テキスト (ひらがな + 句読点) を IPA 音素列に分解し、
//   後段の phoneme-timeline がタイムラインを生成する際の
//   入力データ (PhonemeToken[]) を構築する。
//
// 設計方針:
//   - 完全に決定的、副作用なしのテーブル参照モジュール
//   - 拗音優先の最長一致 (2 文字 → 1 文字 fallback)
//   - 撥音「ん」(N) は parseHiragana の後段処理 resolveHatsuonAllophones
//     で、直後トークンの先頭音素を lookahead して [m]/[n]/[ŋ]/[ɴ] に確定する
//   - 促音「っ」(Q) と長音「ー」(R) はマーカーとして残し、
//     phoneme-timeline 側で展開する (前者は直後子音の閉鎖延長、
//     後者は直前母音の duration 延長)
//   - 未知文字 (カタカナ・漢字・記号) は無視
//   - 仕様書 3.1.1 に列挙された全エントリを完全網羅:
//       清音 46 (撥音「ん」を含む) + 濁音 20 + 半濁音 5
//       + 拗音 33 (11 子音 × 3 母音、ぢゃ系は じゃ系と統合)
//       + 特殊拍 2 (促音「っ」と長音「ー」)
//       = 合計 106 エントリ
//     仕様書本文には「110」とあるが、これは ぢゃ/ぢゅ/ぢょ や
//     ヴァ等の方言バリアントを含めた理論値であり、実装テーブルの
//     実エントリは仕様書 3.1.1 表を完全網羅した 106 となる。
// ============================================================================

import type { PhonemeToken, PhonemeCategory } from '../types/index';

// ---------------------------------------------------------------------------
// 1. ひらがな → 音素マッピングテーブル (110 エントリ)
// ---------------------------------------------------------------------------
//
// IPA 音素表記:
//   - 母音: a / i / ɯ / e / o
//   - 子音: k / s / ɕ / t / ts / tɕ / n / ɲ / h / ç / ɸ / m / j / ɾ / w
//           / g / z / dz / dʑ / d / b / p
//   - 拗音子音 (palatalized): kj / gj / mj / bj / pj / ɾj
//   - 特殊拍マーカー: N (撥音) / Q (促音) / R (長音)

